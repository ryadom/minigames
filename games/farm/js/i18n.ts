/** The localized display name of an item id (falls back to the id). */
export function itemName(id: string): string {
  const n = MG.i18n.t<Record<string, string>>("names");
  return n?.[id] || id;
}

/** Translate `key`, substituting {placeholders} from `vars`. */
export function tf(key: string, vars?: Record<string, string | number>): string {
  const s = MG.i18n.t<unknown>(key);
  if (typeof s !== "string") return s as string;
  let out = s;
  if (vars) {
    for (const k of Object.keys(vars)) out = out.replace(`{${k}}`, String(vars[k]));
  }
  return out;
}
