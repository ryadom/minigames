  function name(id) { var n = MG.i18n.t("names"); return (n && n[id]) || id; }
  function tf(key, vars) {
    var s = MG.i18n.t(key);
    if (typeof s !== "string") return s;
    for (var k in vars) if (vars.hasOwnProperty(k)) s = s.replace("{" + k + "}", vars[k]);
    return s;
  }

